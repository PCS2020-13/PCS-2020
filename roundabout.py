import numpy as np

class Roundabout():
    def __init__(self):
        self.exceptions = []
        self.area = []
        self.queue = np.array(([]))

    # Gets all the indexes between 2 points in the matrix.
    def get_area(self, area, x, y):
        x1, y1 = x
        x2, y2 = y
        for i in range(x1, x2+1):
            for j in range(y1, y2+1):
                area.append((i,j))
        return area


